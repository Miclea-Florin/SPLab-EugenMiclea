package com.example.splabeugenmiclea.Classes;

public class Paraghrap {
    String text;

    public Paraghrap(String text) {
        this.text = text;
    }

    @Override
    public String toString() {
        return "Paraghrap{" +  "\n" +
                "text='" + text + '\'' +
                '}';
    }
}
